pub fn search_color_name(hex: &str) -> Option<String> {
    COLOR_NAMES.get(hex).map(|s| s.to_string())
}

pub fn search_color_code(name: &str) -> Option<String> {
    COLOR_CODES.get(name).map(|s| s.to_string())
}

pub fn search_names(querys: Vec<String>) -> Vec<String> {
    let mut names = COLOR_NAMES_ARRAY.map(|x| x.to_ascii_lowercase()).to_vec();
    for q in querys {
        names = names
            .into_iter()
            .filter(|x| (*x).contains(&q.to_ascii_lowercase()))
            .collect();
    }
    names.iter().map(|x| to_camel(x)).collect()
}

fn to_camel(name: &str) -> String {
    let mut result = "".to_string();
    for word in name.split_ascii_whitespace() {
        let mut new_word = "".to_string();
        for (i, c) in word.chars().enumerate() {
            if i == 0 {
                new_word.push(c.to_ascii_uppercase());
            } else {
                new_word.push(c);
            }
        }
        result.push_str(&new_word);
        result.push(' ');
    }
    result.trim().to_string()
}

